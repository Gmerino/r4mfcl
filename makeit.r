R4MFCL 0.2 2013_09_16 for R 3.1
An updated version of R4MFCL with better help and manual, including a brief description of each function, and identifying the authors. I removed several functions that are redundant and probably not used, and updated several (read.catchrep, and read.frq) to fix bugs.
Note that R4MFCL will need to be substantially updated when the new version of MFCL arrives, because it has new structures for the input and output files.

